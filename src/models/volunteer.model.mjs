import { Schema, model } from "mongoose";

const volunteerSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Volunteer = model("Volunteer", volunteerSchema);

export default Volunteer;
